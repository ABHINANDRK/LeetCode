# Output: true

class Sudoku:
    def __init__(self,board):
        self.board = board

    def test(self):

        big = set()
        for i in range(0,9):
            for j in range(0,9):
                if board[i][j]!='.':
                    cur = board[i][j]
                    big.add((i,cur))
                    big.add((cur,j))
                    big.add((i/3,j/3,cur))
        print(big)






